#include <stdio.h>

void ft_swap(int *a, int *b)
{
	int x = *a;
	int s = *b;
	*a = s;
	*b = x;
}

int main()
{
	int x = 5;
	int s = 3;
	printf("x = %d s = %d\n",x,s);
	ft_swap(&x, &s);
	printf("x = %d s = %d",x,s);
	return 0;
}
